package com.hyva.idm.sass.sassentities;

import com.hyva.idm.entity.BaseEntity;
import lombok.Data;
import org.hibernate.annotations.GenericGenerator;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;

@Entity

public class Webpermission {

    @Id
    @GeneratedValue(strategy = GenerationType.AUTO,generator = "native")
    @GenericGenerator(name = "native", strategy = "native")
    private Long webid;
    private String tagName;
    private String className;
    private String ele_height;
    private String ele_width;
    private String ele_top;
    private String ele_left;
    private String position;
    private String background_color;
    private String color;
    private String font_size;
    private String text_align;
    private  String applicationName;
    private  String tableName;
    private String pageName;

    public String getApplicationName() {
        return applicationName;
    }

    public void setApplicationName(String applicationName) {
        this.applicationName = applicationName;
    }

    public String getTableName() {
        return tableName;
    }

    public void setTableName(String tableName) {
        this.tableName = tableName;
    }

    public String getPageName() {
        return pageName;
    }

    public void setPageName(String pageName) {
        this.pageName = pageName;
    }

    public Long getWebid() {
        return webid;
    }

    public void setWebid(Long webid) {
        this.webid = webid;
    }

    public String getTagName() {
        return tagName;
    }

    public void setTagName(String tagName) {
        this.tagName = tagName;
    }

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

    public String getEle_height() {
        return ele_height;
    }

    public void setEle_height(String ele_height) {
        this.ele_height = ele_height;
    }

    public String getEle_width() {
        return ele_width;
    }

    public void setEle_width(String ele_width) {
        this.ele_width = ele_width;
    }

    public String getEle_top() {
        return ele_top;
    }

    public void setEle_top(String ele_top) {
        this.ele_top = ele_top;
    }

    public String getEle_left() {
        return ele_left;
    }

    public void setEle_left(String ele_left) {
        this.ele_left = ele_left;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public String getBackground_color() {
        return background_color;
    }

    public void setBackground_color(String background_color) {
        this.background_color = background_color;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getFont_size() {
        return font_size;
    }

    public void setFont_size(String font_size) {
        this.font_size = font_size;
    }

    public String getText_align() {
        return text_align;
    }

    public void setText_align(String text_align) {
        this.text_align = text_align;
    }
}
