package com.hyva.idm.sass.sassmapper;

import com.hyva.idm.sass.sassentities.Tickets;
import com.hyva.idm.sass.sasspojo.TicketsPojo;

public class TicketsMapper {
    public static Tickets mapPojoToEntity(TicketsPojo ticketsPojo){
        Tickets tickets = new Tickets();
        tickets.setId( ticketsPojo.getId());
        tickets.setName( ticketsPojo.getName());
        tickets.setEmail( ticketsPojo.getEmail());
        return tickets;
    }
}
