package com.hyva.idm.sass.sassmapper;

import com.hyva.idm.sass.sassentities.Action;
import com.hyva.idm.sass.sassentities.Position;
import com.hyva.idm.sass.sasspojo.ActionPojo;
import com.hyva.idm.sass.sasspojo.PositionPojo;

/**
 * Created by krishna.
 */
public class PositionMapper {

    public static Position mapActionPojoToEnity(PositionPojo positionPojo){
        Position position  = new Position();
        position.setId(positionPojo.getId());
        position.setName(positionPojo.getName());
        position.setDescription(positionPojo.getDescription());
        position.setStatus(positionPojo.getStatus());
        return  position;
    }
}
